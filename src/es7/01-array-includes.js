//"array includes" lets you know if an array contains an element
let numbers = [1, 3, 4, 6, 7, 8];

console.log(numbers.includes(4));
console.log(numbers.includes(2));

let names = ["Kevin", "Alexis", "Paola", "Diana"];

//The capital letter affects the result
console.log(names.includes("Kevin"));

console.log(names.includes("kevin"));

